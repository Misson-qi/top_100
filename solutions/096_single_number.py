# 力扣 热题100 · 第96题 只出现一次的数字
# 链接: https://leetcode.cn/problems/single-number/
#
# ---------- 思路 ----------
# 题目在问什么：数组里除了一个数出现一次外其余都出现两次，找那个只出现一次的数。要求 O(n) 时间 O(1) 空间。
#
# 做法：异或。a^a=0，a^0=a，且异或满足交换律结合律。把所有数异或起来，出现两次的会两两消成 0，最后剩下的就是只出现一次的数。
#
# ---------- 关键点 ----------
# · 异或运算的性质：同一个数异或两次等于 0（a^a=0），任何数和 0 异或等于自己（a^0=a），而且异或满足交换律、结合律。所以把数组中所有数依次异或起来，出现两次的数会两两配对异或成 0，最后只剩下出现一次的那个数。只用一个变量累加异或，空间 O(1)。

from typing import List


class Solution:
    def singleNumber(self, nums: List[int]) -> int:
        ans = 0
        for x in nums:
            ans ^= x
        return ans
