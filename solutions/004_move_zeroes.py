# 力扣 热题100 · 第4题 移动零
# 链接: https://leetcode.cn/problems/move-zeroes/
#
# ---------- 思路 ----------
# 题目在问什么：把数组里的 0 都挪到末尾，非零数保持相对顺序，且要原地修改。
#
# 做法：想象从左到右「填坑」。用一个指针 j 表示「下一个非零数该放的位置」。遍历数组，遇到非零数就和 j 位置交换，然后 j 往后移一位。这样非零数会按顺序填到前面，0 自然被挤到后面。
#
# ---------- 关键点 ----------
# · j 表示「下一个非零数该放到的位置」。在 j 前面的都已经是非零数且顺序不变；从 j 到当前 i 之间要么是 0，要么是还没「往前挪」的非零。遇到非零数就和 nums[j] 交换，然后 j 往后移一位，相当于把这个非零数放到 j 的位置，j 再指向下一个空位。
# · 交换不会打乱顺序：因为我们是从左到右扫的，先遇到的非零会先被换到更前面的 j；交换时 nums[j] 要么是 0（换过去后 0 到了后面），要么是更早的非零，不会把「先出现的非零」换到「后出现的非零」后面。
# · 必须从左往右用 j 填：题目要求非零数的相对顺序不变。若允许乱序，可以用左右指针一头一尾交换 0 和非零；这里只能让 j 从左往后，遇到非零就往前填。

from typing import List


class Solution:
    def moveZeroes(self, nums: List[int]) -> None:
        j = 0
        for i in range(len(nums)):
            if nums[i] != 0:
                nums[j], nums[i] = nums[i], nums[j]
                j += 1
