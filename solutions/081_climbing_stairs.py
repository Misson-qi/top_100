# 力扣 热题100 · 第81题 爬楼梯
# 链接: https://leetcode.cn/problems/climbing-stairs/
#
# ---------- 思路 ----------
# 题目在问什么：n 阶楼梯，每次可爬 1 或 2 阶，求到顶的不同方法数。
#
# 做法：到第 n 阶要么从 n-1 跨 1 步，要么从 n-2 跨 2 步，所以 f(n)=f(n-1)+f(n-2)。用两个变量滚动，不用开数组。
#
# ---------- 关键点 ----------
# · 到第 n 阶只有两种可能：要么从第 n-1 阶跨 1 步上来，要么从第 n-2 阶跨 2 步上来。所以「到 n 阶的方法数」=「到 n-1 阶的方法数」+「到 n-2 阶的方法数」，即 f(n)=f(n-1)+f(n-2)。这就是斐波那契递推。因为只用到前两项，用两个变量 a、b 轮流更新即可，不需要开一整条数组。

class Solution:
    def climbStairs(self, n: int) -> int:
        if n <= 2:
            return n
        a, b = 1, 2
        for _ in range(3, n + 1):
            a, b = b, a + b
        return b
