# 力扣 热题100 · 第91题 不同路径
# 链接: https://leetcode.cn/problems/unique-paths/
#
# ---------- 思路 ----------
# 题目在问什么：m×n 网格从左上到右下，只能向右或向下，求路径数。
#
# 做法：dp[i][j] 表示到 (i,j) 的路径数。只能从 (i-1,j) 或 (i,j-1) 来，所以 dp[i][j]=dp[i-1][j]+dp[i][j-1]。边界 dp[0][0]=1 或第一行第一列全 1。可压成一维：dp[j] += dp[j-1]。
#
# ---------- 关键点 ----------
# · 到 (i,j) 的走法只有两种：从上面 (i-1,j) 向下走一步，或从左边 (i,j-1) 向右走一步。所以「到 (i,j) 的路径数」=「到 (i-1,j) 的路径数」+「到 (i,j-1) 的路径数」，即 dp[i][j]=dp[i-1][j]+dp[i][j-1]。第一行和第一列只有一条路（一直右或一直下），所以都是 1。可以压成一维：按行从左到右更新，更新前 dp[j] 存的是上一行的值（相当于从上来），dp[j-1] 已是本行刚算过的（相当于从左来），所以 dp[j]+=dp[j-1] 就得到当前格的路径数。

class Solution:
    def uniquePaths(self, m: int, n: int) -> int:
        dp = [1] * n
        for _ in range(1, m):
            for j in range(1, n):
                dp[j] += dp[j - 1]
        return dp[-1]
