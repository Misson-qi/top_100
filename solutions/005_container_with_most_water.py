# 力扣 热题100 · 第5题 盛最多水的容器
# 链接: https://leetcode.cn/problems/container-with-most-water/
#
# ---------- 思路 ----------
# 题目在问什么：给一排竖线的高度，选两条线和 x 轴围成容器，求能盛水的最大面积。面积 = 底宽 × 较矮的那条线高。
#
# 做法：左右两个指针 i、j 从两端往中间走。当前面积 = (j-i) × min(h[i], h[j])。每次把「较矮」的那一侧指针往中间移一步（因为矮的一边限制了高度，移高的一边面积只会不变或变小，所以只移矮边才有机会变大）。记录过程中的最大面积。
#
# ---------- 关键点 ----------
# · 为什么每次只移动矮的那一边？因为高度由矮边决定，移动高边的话：底宽变短 1，高度又不会变（高边再高也没用），面积只会变小。只有移动矮边，才可能在新位置遇到更高的边，面积才有机会变大。所以贪心策略就是：哪边矮就移哪边。
# · 这样会不会漏掉最优解？不会。假设最优解是 (L, R)。我们的右指针会从最右边一路往左移，总有一刻会到 R；那时左指针要么还没到 L、要么已经在 L。若已经在 L，就会算到 (L,R) 的面积；若还没到 L，之后左指针会往右移，也会经过 L，那时右指针还在 R 或更右，所以 (L,R) 一定会在某次被算到。

from typing import List


class Solution:
    def maxArea(self, height: List[int]) -> int:
        i, j = 0, len(height) - 1
        ans = 0
        while i < j:
            ans = max(ans, (j - i) * min(height[i], height[j]))
            if height[i] <= height[j]:
                i += 1
            else:
                j -= 1
        return ans
